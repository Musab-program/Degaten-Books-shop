CREATE TABLE Authors (
    Author_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ַבדַֽ ַבֳ׃ַ׃ם
    Name VARCHAR2(100) NOT NULL, -- ַ׃ד ַבדִב
    Nationality VARCHAR2(50), -- ַבּה׃םֹ
    Birth_Year NUMBER(4) -- ׃הֹ ַבדםבַֿ
);

-- ֵהװֱַ ּֿזב ַבױהםַ
CREATE TABLE Categories (
    Category_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ַבדַֽ ַבֳ׃ַ׃ם
    Category_Name VARCHAR2(100) NOT NULL -- ַ׃ד ַבױהם
);

-- ֵהװֱַ ּֿזב ַבָ
CREATE TABLE Books (
    Book_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ַבדַֽ ַבֳ׃ַ׃ם
    Title VARCHAR2(200) NOT NULL, -- הזַה ַבַָ
    Author_ID NUMBER NOT NULL, -- דׁ ַבדִב
    Category_ID NUMBER NOT NULL, -- דׁ ַבױהם
    ISBN VARCHAR2(20), -- ׁד ַבַָ ַבֿזבם
    Publication_Year NUMBER(4), -- ׃הֹ ַבהװׁ
    Copies_Available NUMBER DEFAULT 0, -- ֿֿ ַבה׃־ ַבדַֹֽ
    CONSTRAINT fk_books_authors FOREIGN KEY (Author_ID) REFERENCES Authors(Author_ID), -- ַבבַֹ ד ּֿזב ַבדִבםה
    CONSTRAINT fk_books_categories FOREIGN KEY (Category_ID) REFERENCES Categories(Category_ID) -- ַבבַֹ ד ּֿזב ַבױהםַ
);

-- ֵהװֱַ ּֿזב ַבֳײֱַ
CREATE TABLE Members (
    Member_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ַבדַֽ ַבֳ׃ַ׃ם
    Name VARCHAR2(100) NOT NULL, -- ַ׃ד ַבײז
    Email VARCHAR2(100) UNIQUE NOT NULL, -- ַבָׁםֿ ַבֵבׁזהם
    Phone VARCHAR2(15), -- ׁד ַבוַ
    Address VARCHAR2(200), -- ַבהזַה
    Membership_Date DATE DEFAULT SYSDATE -- ַׁם־ ַבײזםֹ
);

-- ֵהװֱַ ּֿזב ַבֵַַׁ
CREATE TABLE Borrowing (
    Borrowing_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ַבדַֽ ַבֳ׃ַ׃ם
    Book_ID NUMBER NOT NULL, -- דׁ ַבַָ
    Member_ID NUMBER NOT NULL, -- דׁ ַבײז
    Borrow_Date DATE DEFAULT SYSDATE, -- ַׁם־ ַבֵַֹׁ
    Return_Date DATE, -- ַׁם־ ַבֵַּׁ
    Status VARCHAR2(20) DEFAULT 'םׁ ד׃ֿׁ', -- ַֽבֹ ַבֵַֹׁ
    CONSTRAINT fk_borrowing_books FOREIGN KEY (Book_ID) REFERENCES Books(Book_ID), -- ַבבַֹ ד ּֿזב ַבָ
    CONSTRAINT fk_borrowing_members FOREIGN KEY (Member_ID) REFERENCES Members(Member_ID) -- ַבבַֹ ד ּֿזב ַבֳײֱַ
);
